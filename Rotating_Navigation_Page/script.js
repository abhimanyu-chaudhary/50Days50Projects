function turnup(){
    document.querySelector(".nav").classList.add("nav-after");
    document.querySelector(".container").classList.add("center-body-after");
    document.querySelector(".slider").classList.add("slider-after");
}


function turndown(){
    document.querySelector(".nav").classList.remove("nav-after");
    document.querySelector(".container").classList.remove("center-body-after");
    document.querySelector(".slider").classList.remove("slider-after");
}